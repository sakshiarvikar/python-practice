num1 = 10
num2 = 3

#arithmetic operators
print("num1 + num2 =", num1+num2)
print("num1 - num2 =", num1-num2)
print("num1 * num2 =", num1*num2)
print("num1 / num2 =", num1/num2)
print("num1 ** num2 =", num1**num2) #10 to the power 3
print("num1 // num2 =", num1//num2) #result is not float
print("num1 % num2 =", num1%num2)

